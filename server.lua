local Tunnel = module("vrp", "lib/Tunnel")
local Proxy = module("vrp", "lib/Proxy")

vRP = Proxy.getInterface("vRP")
vRPclient = Tunnel.getInterface("vRP","wild:permission")



RegisterNetEvent("wild:permission")
AddEventHandler("wild:permission", function()
	local user_id = vRP.getUserId({source})

	if vRP.hasGroup({user_id, "Politi-Job"}) then
        TriggerClientEvent("wild:okay", source)
    else
        TriggerClientEvent('mythic_notify:client:SendAlert', source, { type = 'error', text = 'Du ikke en del af SAHP'}, 5000)
	end
end)